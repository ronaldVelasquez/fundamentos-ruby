# Los tipos de datos numericos se dividen en enteros y coma flotante
# Se pueden realizar las operaciones basicas
# Se suma y se imprime
puts 'La suma es :'
puts 2 + 3 # El resultado es => 5
# Se resta y se imprime
puts 'La resta es :'
puts 3 - 2 # El resultado es => 1
# Se multiplica y se imprime
puts 'La multiplicacion es :'
puts 3 * 3 # El resultado es => 9
# Se divide y se imprime
puts 'La division es :'
puts 5 / 2 # El resultado es => 2, esto se debe a que se esta dividiendo entre enteros
# Para que salga el verdadero resultado debe de ser asi:
puts 'La division corregida es :'
puts 5.0 / 2 # Aqui el 5.0 es ya un float, por lo tanto el resultado saldra float => 2.5
# Tambien tenemos el operador mod (%). Da el resto de una division
puts 'El residuo es :'
puts 5 % 2 # El resultado es => 1
# Otro operador es potencia(**). Eleva un numero
puts 'La potencia'
puts 5**2 # El resultado es => 25
